package week1.day1;

public class LearnIfCondition {
	public static void main(String[] args) {
		int number = 45;
		
		if(number>0) {
		System.out.println("The number is Positive");
	}
		else if(number<0) {
			System.out.println("The number is Negative");
		}
		else { 
		System.out.println("The number is Neutral");
		}
	}
}
	



